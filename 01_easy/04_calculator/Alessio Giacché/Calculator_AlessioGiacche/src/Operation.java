public class Operation {

    /**
     * Initialize a new instance of Operation
     */
    public Operation() {
    }

    /**
     * Sum operation
     *
     * @param n1 the first number
     * @param n2 the second number
     * @return the sum between the two number
     */
    public double sum(double n1, double n2) {
        return n1 + n2;
    }

    /**
     * Diff operation
     * @param n1 the first number
     * @param n2 the second number
     * @return the diff between the two number
     */
    public double diff(double n1, double n2) {
        return n1 - n2;
    }

    /**
     * Div operation
     * @param n1 the first number
     * @param n2 the second number
     * @return the div between the two number
     */
    public double div(double n1, double n2) {
        return n1 / n2;
    }

    /**
     * Molt operation
     * @param n1 the first number
     * @param n2 the second number
     * @return the molt between the two number
     */
    public double molt(double n1, double n2) {
        return n1 * n2;
    }
}
